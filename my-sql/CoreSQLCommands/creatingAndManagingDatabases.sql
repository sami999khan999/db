-- ---------------------------
-- CREATE DATABASE
-- ---------------------------

-- This command creates a new database.
-- The database will be created with the specified name.
-- If the database already exists, an error will occur unless "IF NOT EXISTS" is used.
CREATE DATABASE test;

-- ---------------------------
-- DROP DATABASE
-- ---------------------------

-- This command deletes an existing database.
-- The database and all its contents (tables, views, etc.) will be permanently removed.
-- If the database does not exist, an error will occur unless "IF EXISTS" is used.
DROP DATABASE test;
